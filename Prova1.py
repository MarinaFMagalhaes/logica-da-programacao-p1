# 1. Crie um exemplo de cada uma das principais tipagens de variáveis.

# Variavel do tipo string (str) - todo e qualquer texto.

nome = "Namjoon"
nacionalidade = "Coreana"
profissão = "Rapper, cantor e compositor"

print(type(nome))

print(type(nacionalidade))

print(type(profissão))

# Variavel do tipo inteiro (int) - números inteiros.

idade = 29
carreira = 12

print(type(idade))

print(type(carreira))

# Variavel do tipo decimal (float) - números decimais.

altura = 1.81
peso = 77.7

print(type(altura))

print(type(peso))

# Variavel do tipo lógico (bool) - verdadeiro ou falso.

coreano = True
japones = False

print(type(coreano))

print(type(japones))

